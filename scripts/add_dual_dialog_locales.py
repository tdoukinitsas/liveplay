def deep_set(d, dotted_key, value):
    """Set d[a][b][c] = value when dotted_key is 'a.b.c'. Creates dicts."""
    parts = dotted_key.split('.')
    cur = d
    for p in parts[:-1]:
        if p not in cur or not isinstance(cur[p], dict):
            cur[p] = {}
        cur = cur[p]
    cur[parts[-1]] = value


def main():
    locale_dir = os.path.join(os.path.dirname(__file__), 'client', 'locales')
    if not os.path.isdir(locale_dir):
        print(f'ERROR: {locale_dir} not found', file=sys.stderr)
        sys.exit(1)

    for code, strings in TRANSLATIONS.items():
        path = os.path.join(locale_dir, f'{code}.json')
        if not os.path.exists(path):
            print(f'SKIP (no file): {code}')
            continue
        with open(path, 'r', encoding='utf-8') as f:
            data = json.load(f)
        for k, v in strings.items():
            deep_set(data, k, v)
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')
        print(f'Updated: {code} ({len(strings)} keys)')


if __name__ == '__main__':
    main()
